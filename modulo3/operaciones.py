import numpy as np
arr1 = np.array([1, 2, 3, 4, 5])
arr2 = np.array([6, 7, 8, 9, 10])
suma = arr1 + arr2  # Suma elemento a elemento
resta = arr1 - arr2  # Resta elemento a elemento
multiplicacion = arr1 * arr2  # Multiplicación elemento a elemento
division = arr1 / arr2  # División elemento a elemento 
escalar = arr1 * 2  # Multiplicación por un escalar
print("Arreglo 1:", arr1)
print("Arreglo 2:", arr2) 
print("Suma:", suma)
print("Resta:", resta)  
print("Multiplicación:", multiplicacion)
print("División:", division)
print("Multiplicación por escalar:", escalar)